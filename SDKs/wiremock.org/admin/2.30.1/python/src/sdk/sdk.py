
__doc__ = """ SDK Documentation: http://wiremock.org/docs/ - WireMock user documentation"""
import requests

from . import utils

from .near_misses import NearMisses
from .recordings import Recordings
from .requests import Requests
from .scenarios import Scenarios
from .stub_mappings import StubMappings
from .system import System


SERVERS = [
	"https://wiremock.org/",
]


class SDK:
    r"""SDK Documentation: http://wiremock.org/docs/ - WireMock user documentation"""
    near_misses: NearMisses
    recordings: Recordings
    requests: Requests
    scenarios: Scenarios
    stub_mappings: StubMappings
    system: System

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.near_misses = NearMisses(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.recordings = Recordings(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.requests = Requests(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.scenarios = Scenarios(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.stub_mappings = StubMappings(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.system = System(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    