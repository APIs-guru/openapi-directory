
__doc__ = """ SDK Documentation: https://www.openpolicyagent.org/docs/latest/ - OPA documentation"""
import requests

from . import utils

from .compile_api import CompileAPI
from .data_api import DataAPI
from .health_api import HealthAPI
from .policy_api import PolicyAPI
from .query_api import QueryAPI


SERVERS = [
	"http://openpolicy.local",
]


class SDK:
    r"""SDK Documentation: https://www.openpolicyagent.org/docs/latest/ - OPA documentation"""
    compile_api: CompileAPI
    data_api: DataAPI
    health_api: HealthAPI
    policy_api: PolicyAPI
    query_api: QueryAPI

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.compile_api = CompileAPI(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.data_api = DataAPI(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.health_api = HealthAPI(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.policy_api = PolicyAPI(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.query_api = QueryAPI(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    