

import requests

from . import utils

from .fact_of_the_day import FactOfTheDay
from .number_facts import NumberFacts
from .on_this_day import OnThisDay
from .private_facts import PrivateFacts
from .random_facts import RandomFacts


SERVERS = [
	"https://api.fungenerators.com",
]


class SDK:
    
    fact_of_the_day: FactOfTheDay
    number_facts: NumberFacts
    on_this_day: OnThisDay
    private_facts: PrivateFacts
    random_facts: RandomFacts

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.fact_of_the_day = FactOfTheDay(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.number_facts = NumberFacts(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.on_this_day = OnThisDay(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.private_facts = PrivateFacts(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.random_facts = RandomFacts(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    