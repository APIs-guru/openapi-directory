type LicenseTemplate struct {
	_defaultClient  HTTPClient
	_securityClient HTTPClient
	_serverURL      string
	_language       string
	_sdkVersion     string
	_genVersion     string
}

func NewLicenseTemplate(defaultClient, securityClient HTTPClient, serverURL, language, sdkVersion, genVersion string) *LicenseTemplate {
	return &LicenseTemplate{
		_defaultClient:  defaultClient,
		_securityClient: securityClient,
		_serverURL:      serverURL,
		_language:       language,
		_sdkVersion:     sdkVersion,
		_genVersion:     genVersion,
	}
}

// CreateLicenseTemplate - Create License Template
// Creates a new License Template
