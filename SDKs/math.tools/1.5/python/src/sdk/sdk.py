

import requests

from . import utils

from .base_conversion import BaseConversion
from .number_checks import NumberChecks
from .number_facts import NumberFacts
from .number_generation import NumberGeneration
from .number_systems_conversion import NumberSystemsConversion
from .number_of_the_day import NumberOfTheDay
from .pi import Pi
from .prime import Prime
from .spell import Spell


SERVERS = [
	"https://api.math.tools",
	"http://api.math.tools",
]


class SDK:
    
    base_conversion: BaseConversion
    number_checks: NumberChecks
    number_facts: NumberFacts
    number_generation: NumberGeneration
    number_systems_conversion: NumberSystemsConversion
    number_of_the_day: NumberOfTheDay
    pi: Pi
    prime: Prime
    spell: Spell

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.base_conversion = BaseConversion(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.number_checks = NumberChecks(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.number_facts = NumberFacts(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.number_generation = NumberGeneration(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.number_systems_conversion = NumberSystemsConversion(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.number_of_the_day = NumberOfTheDay(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.pi = Pi(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.prime = Prime(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.spell = Spell(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    