

import requests

from . import utils

from .feature import Feature
from .feature_taxonomy import FeatureTaxonomy
from .name import Name
from .name_authority import NameAuthority
from .search import Search


SERVERS = [
	"https://apps.gov.bc.ca/pub/bcgnws",
	"https://test.apps.gov.bc.ca/pub/bcgnws",
	"https://delivery.apps.gov.bc.ca/pub/bcgnws",
]


class SDK:
    
    feature: Feature
    feature_taxonomy: FeatureTaxonomy
    name: Name
    name_authority: NameAuthority
    search: Search

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.feature = Feature(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.feature_taxonomy = FeatureTaxonomy(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.name = Name(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.name_authority = NameAuthority(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.search = Search(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    