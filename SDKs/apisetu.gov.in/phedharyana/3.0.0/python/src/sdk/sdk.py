    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        
    

    def config_client(self, client: requests.Session):
        self._client = client
        
    
    
    
    def etcer(self, request: operations.EtcerRequest) -> operations.EtcerResponse:
        r"""Enlistment Certificate
        API to verify Enlistment Certificate.
        """
        
        base_url = self._server_url
        
        url = base_url.removesuffix("/") + "/etcer/certificate"
        
        headers = {}
        req_content_type, data, form = utils.serialize_request_body(request)
        if req_content_type != "multipart/form-data" and req_content_type != "multipart/mixed":
            headers["content-type"] = req_content_type
        
        client = utils.configure_security_client(self._client, request.security)
        
        r = client.request("POST", url, data=data, files=form, headers=headers)
        content_type = r.headers.get("Content-Type")

        res = operations.EtcerResponse(status_code=r.status_code, content_type=content_type)
        
        if r.status_code == 200:
            pass
