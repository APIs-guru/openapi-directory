from typing import Optional
from sdk.models import operations
from . import utils




SERVERS = [
	"https://apisetu.gov.in/phedharyana/v3",
]


class SDK:
    

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
