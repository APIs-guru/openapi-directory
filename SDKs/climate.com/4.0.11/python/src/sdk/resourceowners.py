        base_url = self._server_url
        
        url = utils.generate_url(base_url, "/v4/resourceOwners/{resourceOwnerId}", request.path_params)
        
        
        client = utils.configure_security_client(self._client, request.security)
        
        r = client.request("GET", url)
        content_type = r.headers.get("Content-Type")

        res = operations.GetResourceOwnerResponse(status_code=r.status_code, content_type=content_type)
        
        if r.status_code == 200:
            if utils.match_content_type(content_type, "application/json"):
                out = utils.unmarshal_json(r.text, Optional[shared.ResourceOwner])
                res.resource_owner = out
        elif r.status_code == 400:
            res.headers = r.headers
            
