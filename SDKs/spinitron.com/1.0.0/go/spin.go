func NewSpin(defaultClient, securityClient HTTPClient, serverURL, language, sdkVersion, genVersion string) *Spin {
	return &Spin{
		_defaultClient:  defaultClient,
		_securityClient: securityClient,
		_serverURL:      serverURL,
		_language:       language,
		_sdkVersion:     sdkVersion,
		_genVersion:     genVersion,
	}
}

