
__doc__ = """ SDK Documentation: https://openbuildservice.org/help/ - Open Build Service - Documentation"""
import requests

from . import utils

from .attribute_namespaces import AttributeNamespaces
from .attributes import Attributes
from .build import Build
from .configuration import Configuration
from .distributions import Distributions
from .general_information import GeneralInformation
from .groups import Groups
from .issue_trackers import IssueTrackers
from .person import Person
from .published_binaries import PublishedBinaries
from .request import Request
from .workers import Workers


SERVERS = [
	"https://opensuse.org/",
]


class SDK:
    r"""SDK Documentation: https://openbuildservice.org/help/ - Open Build Service - Documentation"""
    attribute_namespaces: AttributeNamespaces
    attributes: Attributes
    build: Build
    configuration: Configuration
    distributions: Distributions
    general_information: GeneralInformation
    groups: Groups
    issue_trackers: IssueTrackers
    person: Person
    published_binaries: PublishedBinaries
    request: Request
    workers: Workers

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.attribute_namespaces = AttributeNamespaces(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.attributes = Attributes(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.build = Build(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.configuration = Configuration(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.distributions = Distributions(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.general_information = GeneralInformation(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.groups = Groups(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.issue_trackers = IssueTrackers(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.person = Person(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.published_binaries = PublishedBinaries(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.request = Request(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.workers = Workers(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    