
__doc__ = """ SDK Documentation: https://developers.apideck.com - Apideck Developer Docs"""
import requests
from sdk.models import shared
from . import utils

from .companies import Companies
from .departments import Departments
from .employee_payrolls import EmployeePayrolls
from .employee_schedules import EmployeeSchedules
from .employees import Employees
from .jobs import Jobs
from .payrolls import Payrolls
from .time_off_requests import TimeOffRequests


SERVERS = [
	"https://unify.apideck.com",
]


class SDK:
    r"""SDK Documentation: https://developers.apideck.com - Apideck Developer Docs"""
    companies: Companies
    departments: Departments
    employee_payrolls: EmployeePayrolls
    employee_schedules: EmployeeSchedules
    employees: Employees
    jobs: Jobs
    payrolls: Payrolls
    time_off_requests: TimeOffRequests

    _client: requests.Session
    _security_client: requests.Session
    _security: shared.Security
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        
        if self._security is not None:
            self._security_client = utils.configure_security_client(self._client, self._security)
        self._init_sdks()
    

    def config_security(self, security: shared.Security):
        self._security = security
        self._security_client = utils.configure_security_client(self._client, security)
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.companies = Companies(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.departments = Departments(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.employee_payrolls = EmployeePayrolls(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.employee_schedules = EmployeeSchedules(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.employees = Employees(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.jobs = Jobs(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.payrolls = Payrolls(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.time_off_requests = TimeOffRequests(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    