func NewConnectors(defaultClient, securityClient HTTPClient, serverURL, language, sdkVersion, genVersion string) *Connectors {
	return &Connectors{
		_defaultClient:  defaultClient,
		_securityClient: securityClient,
		_serverURL:      serverURL,
		_language:       language,
		_sdkVersion:     sdkVersion,
		_genVersion:     genVersion,
	}
}

