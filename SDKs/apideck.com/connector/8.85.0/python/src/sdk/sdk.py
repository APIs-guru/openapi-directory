
__doc__ = """ SDK Documentation: https://developers.apideck.com - Apideck Developer Docs"""
import requests
from sdk.models import shared
from . import utils

from .api_resources import APIResources
from .apis import ApIs
from .connector_docs import ConnectorDocs
from .connector_resources import ConnectorResources
from .connectors import Connectors


SERVERS = [
	"https://unify.apideck.com",
]


class SDK:
    r"""SDK Documentation: https://developers.apideck.com - Apideck Developer Docs"""
    api_resources: APIResources
    ap_is: ApIs
    connector_docs: ConnectorDocs
    connector_resources: ConnectorResources
    connectors: Connectors

    _client: requests.Session
    _security_client: requests.Session
    _security: shared.Security
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        
        if self._security is not None:
            self._security_client = utils.configure_security_client(self._client, self._security)
        self._init_sdks()
    

    def config_security(self, security: shared.Security):
        self._security = security
        self._security_client = utils.configure_security_client(self._client, security)
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.api_resources = APIResources(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.ap_is = ApIs(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.connector_docs = ConnectorDocs(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.connector_resources = ConnectorResources(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.connectors = Connectors(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    