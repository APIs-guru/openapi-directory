

import requests
from sdk.models import shared
from . import utils

from .administrations import Administrations
from .discovery import Discovery
from .documentsubmissions import DocumentSubmissions
from .invoicesubmissions import InvoiceSubmissions
from .legalentities import LegalEntities
from .peppolidentifiers import PeppolIdentifiers
from .purchaseinvoices import PurchaseInvoices
from .webhookinstances import WebhookInstances


SERVERS = [
	"https://api.storecove.com/api/v2",
]


class SDK:
    
    administrations: Administrations
    discovery: Discovery
    document_submissions: DocumentSubmissions
    invoice_submissions: InvoiceSubmissions
    legal_entities: LegalEntities
    peppol_identifiers: PeppolIdentifiers
    purchase_invoices: PurchaseInvoices
    webhook_instances: WebhookInstances

    _client: requests.Session
    _security_client: requests.Session
    _security: shared.Security
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        
        if self._security is not None:
            self._security_client = utils.configure_security_client(self._client, self._security)
        self._init_sdks()
    

    def config_security(self, security: shared.Security):
        self._security = security
        self._security_client = utils.configure_security_client(self._client, security)
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.administrations = Administrations(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.discovery = Discovery(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.document_submissions = DocumentSubmissions(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.invoice_submissions = InvoiceSubmissions(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.legal_entities = LegalEntities(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.peppol_identifiers = PeppolIdentifiers(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.purchase_invoices = PurchaseInvoices(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.webhook_instances = WebhookInstances(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    