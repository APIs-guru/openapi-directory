
__doc__ = """ SDK Documentation: https://cloud.google.com/bigquery/"""
import requests

from . import utils

from .datasets import Datasets
from .jobs import Jobs
from .models import Models
from .projects import Projects
from .routines import Routines
from .rowaccesspolicies import RowAccessPolicies
from .tabledata import Tabledata
from .tables import Tables


SERVERS = [
	"https://bigquery.googleapis.com/bigquery/v2",
]


class SDK:
    r"""SDK Documentation: https://cloud.google.com/bigquery/"""
    datasets: Datasets
    jobs: Jobs
    models: Models
    projects: Projects
    routines: Routines
    row_access_policies: RowAccessPolicies
    tabledata: Tabledata
    tables: Tables

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.datasets = Datasets(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.jobs = Jobs(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.models = Models(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.projects = Projects(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.routines = Routines(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.row_access_policies = RowAccessPolicies(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.tabledata = Tabledata(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.tables = Tables(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    