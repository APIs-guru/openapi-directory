
__doc__ = """ SDK Documentation: https://cloud.google.com/support/docs/apis"""
import requests

from . import utils

from .caseclassifications import CaseClassifications
from .cases import Cases
from .media import Media


SERVERS = [
	"https://cloudsupport.googleapis.com/",
]


class SDK:
    r"""SDK Documentation: https://cloud.google.com/support/docs/apis"""
    case_classifications: CaseClassifications
    cases: Cases
    media: Media

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.case_classifications = CaseClassifications(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.cases = Cases(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.media = Media(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    