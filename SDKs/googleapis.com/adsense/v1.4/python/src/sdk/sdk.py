
__doc__ = """ SDK Documentation: https://developers.google.com/adsense/management/"""
import requests

from . import utils

from .accounts import Accounts
from .adclients import Adclients
from .adunits import Adunits
from .alerts import Alerts
from .customchannels import Customchannels
from .metadata import Metadata
from .payments import Payments
from .reports import Reports
from .savedadstyles import Savedadstyles
from .urlchannels import Urlchannels


SERVERS = [
	"https://www.googleapis.com/adsense/v1.4",
]


class SDK:
    r"""SDK Documentation: https://developers.google.com/adsense/management/"""
    accounts: Accounts
    adclients: Adclients
    adunits: Adunits
    alerts: Alerts
    customchannels: Customchannels
    metadata: Metadata
    payments: Payments
    reports: Reports
    savedadstyles: Savedadstyles
    urlchannels: Urlchannels

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.accounts = Accounts(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.adclients = Adclients(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.adunits = Adunits(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.alerts = Alerts(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.customchannels = Customchannels(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.metadata = Metadata(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.payments = Payments(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.reports = Reports(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.savedadstyles = Savedadstyles(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.urlchannels = Urlchannels(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    