
__doc__ = """ SDK Documentation: https://developers.google.com/admin-sdk/"""
import requests

from . import utils

from .activities import Activities
from .channels import Channels
from .customerusagereports import CustomerUsageReports
from .entityusagereports import EntityUsageReports
from .userusagereport import UserUsageReport


SERVERS = [
	"https://admin.googleapis.com/",
]


class SDK:
    r"""SDK Documentation: https://developers.google.com/admin-sdk/"""
    activities: Activities
    channels: Channels
    customer_usage_reports: CustomerUsageReports
    entity_usage_reports: EntityUsageReports
    user_usage_report: UserUsageReport

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.activities = Activities(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.channels = Channels(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.customer_usage_reports = CustomerUsageReports(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.entity_usage_reports = EntityUsageReports(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.user_usage_report = UserUsageReport(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    