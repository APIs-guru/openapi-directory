
__doc__ = """ SDK Documentation: https://cloud.google.com/identity-platform"""
import requests

from . import utils

from .accounts import Accounts
from .defaultsupportedidps import DefaultSupportedIdps
from .projects import Projects


SERVERS = [
	"https://identitytoolkit.googleapis.com/",
]


class SDK:
    r"""SDK Documentation: https://cloud.google.com/identity-platform"""
    accounts: Accounts
    default_supported_idps: DefaultSupportedIdps
    projects: Projects

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.accounts = Accounts(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.default_supported_idps = DefaultSupportedIdps(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.projects = Projects(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    