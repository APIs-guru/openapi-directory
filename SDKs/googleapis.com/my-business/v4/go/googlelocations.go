type GoogleLocations struct {
	_defaultClient  HTTPClient
	_securityClient HTTPClient
	_serverURL      string
	_language       string
	_sdkVersion     string
	_genVersion     string
}

func NewGoogleLocations(defaultClient, securityClient HTTPClient, serverURL, language, sdkVersion, genVersion string) *GoogleLocations {
	return &GoogleLocations{
		_defaultClient:  defaultClient,
		_securityClient: securityClient,
		_serverURL:      serverURL,
		_language:       language,
		_sdkVersion:     sdkVersion,
		_genVersion:     genVersion,
	}
}

// MybusinessGoogleLocationsReport - Report a GoogleLocation.
