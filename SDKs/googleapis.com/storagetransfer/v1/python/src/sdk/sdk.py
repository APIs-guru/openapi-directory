
__doc__ = """ SDK Documentation: https://cloud.google.com/storage-transfer/docs"""
import requests

from . import utils

from .googleserviceaccounts import GoogleServiceAccounts
from .projects import Projects
from .transferjobs import TransferJobs
from .transferoperations import TransferOperations


SERVERS = [
	"https://storagetransfer.googleapis.com/",
]


class SDK:
    r"""SDK Documentation: https://cloud.google.com/storage-transfer/docs"""
    google_service_accounts: GoogleServiceAccounts
    projects: Projects
    transfer_jobs: TransferJobs
    transfer_operations: TransferOperations

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.google_service_accounts = GoogleServiceAccounts(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.projects = Projects(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.transfer_jobs = TransferJobs(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.transfer_operations = TransferOperations(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    