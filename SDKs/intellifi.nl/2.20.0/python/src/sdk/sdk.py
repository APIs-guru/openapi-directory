SERVERS = [
	"https://{protocol}://{customer}.intellifi.{tld}/api",
]


class SDK:
    r"""SDK Documentation: https://intellifi.zendesk.com - Find out more"""
    authinfo: Authinfo
    blobs: Blobs
    events: Events
    items: Items
    keys: Keys
    kvpairs: Kvpairs
